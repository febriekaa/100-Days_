package day.pkg37;

public class Day37 {

    public static void main(String[] args) {

        String[] namaArray = {"dyo", "pcy", "osh", "jae", "hae"};

        // Menampilkan setiap nama dalam array
        System.out.println("Nama dalam array:");
        for (String nama : namaArray) {
            System.out.println(nama);
        }

        // Menampilkan panjang (jumlah elemen) dari array
        System.out.println("Panjang array: " + namaArray.length);
    
    
    
    }
    
 }
 
