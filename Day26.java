package day.pkg26;

public class Day26 {

    public static void main(String[] args) {
//       Operator penugasan (assignment operator)

/*   x = a
     x += b  # a+b
     x -= b  # a-b
     x *= b  # a*b
     x /= b  # a/b
     x %= b  # a%b     */

        int a = 100;    
    a += 12;
    System.out.println("a + 12  = "+a);
    int b = 77;
    b -= 18;
    System.out.println("b - 18  = "+b);
    int c = 20;
    c *= 25;
    System.out.println("c x 25  = "+c); 
    int d = 144;
    d /= 12;
    System.out.println("d : 12 = "+d); 
    int e = 155;
    e %= 4;
    System.out.println("e % 3  = "+e);
    
    
    }
    
}
