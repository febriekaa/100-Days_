package day.pkg39;

public class Day39 {

    public static void main(String[] args) {
         int[] angkaArray = {20, 40, 60, 80, 100};

        // Menampilkan setiap angka dalam array menggunakan while
        System.out.println("Angka dalam array (while): ");
        int i = 0;
        while (i < angkaArray.length) {
            System.out.println(angkaArray[i]);
            i++;
        }
    }
    
}
